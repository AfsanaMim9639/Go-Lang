package main

import "fmt"

func main() {
    fmt.Println("Hello, World!")
}

/*
package main → Defines the package name. Every Go program starts with a main package for execution.

import "fmt" → Imports the fmt (format) package for input/output.

func main() → The entry point of the program.

fmt.Println("Hello, World!") → Prints text followed by a new line.
*/
